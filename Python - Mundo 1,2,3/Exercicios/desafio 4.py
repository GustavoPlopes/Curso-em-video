a = input('Digite algo: ')
print(f'O tipo primitivo desse valor é: {type(a)}')
print(f'O seu texto está em letra minuscula? {a.islower()}')
print(f'O seu texto está em letra maiúscula? {a.isupper()}')
print(f'O seu texto está em letra alfanumerico? {a.isalnum()}')
print(f'O seu texto está em letra? {a.isalpha()}')
print(f'O seu texto está em espaços? {a.isspace()}')



